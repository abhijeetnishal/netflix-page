var a=1;
function showorhide1(){
    if(a==1){
        document.getElementById("greyboxout-1").style.display="flex";
        document.getElementById('plus-btn1').style.transform='rotate(45deg)';
        a=0;
    }
    else{
        document.getElementById("greyboxout-1").style.display="none";
        document.getElementById('plus-btn1').style.transform='rotate(90deg)';
        a=1;
    }
}
